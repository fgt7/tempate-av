//Next post:
$(".next-bokep").click(function(e){
  e.preventDefault();
  if (thisElement2<dataLength-1) {
    thisElement2 = parseInt(thisElement2) + 1;
    innerContent2(thisElement2);
    dissBtn();
  };
});

//Prev post:
$(".prev-bokep").click(function(e){
  e.preventDefault();
  if (thisElement2>0) {
    thisElement2 = parseInt(thisElement2) - 1;
    innerContent2(thisElement2);
    dissBtn();
  };
});

//Button disable:
function dissBtn(){
  $(".prev-bokep, .next-bokep").removeClass("disabled");
  if (thisElement2<=0){
    $(".prev-bokep").addClass("disabled");
  }
  else if (thisElement2>=dataLength-1){
    $(".next-bokep").addClass("disabled");
  };
};

