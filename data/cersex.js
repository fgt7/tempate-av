var thisElement = 0;

function innerContent(content){
  $(".inner-title").html(post[content].title);
  $(".inner-poster").attr("src",post[content].poster);
  $(".inner-konten").html(post[content].konten);
  $(".inner-tanggal").html(post[content].tanggal);
};

//Open post:
$(".button").click(function(e){
  e.preventDefault();
  thisElement = $(this).attr("data-obj");
  innerContent(thisElement);
  $("#search-bar").css({"display":"none"});
  $("#list-cerita").css({"display":"none"});
  $(".slider").css({"display":"none"});
  $("#cersex-lainnya").css({"display":"none"});
  $("#post-blog").css({"display":"block"});
  $("#judul-awal").css({"display":"none"});
  $(window).scrollTop(0);
  dissBtn();
});

$(".prev-post").on("click", function() {
    $(window).scrollTop(0);
});

$(".next-post").on("click", function() {
    $(window).scrollTop(0);
});

$(".close-post").on("click", function() {
    $(window).scrollTop(0);
});

//Close post:
$(".close-post, .modal-sandbox").click(function(){
  $("#search-bar").css({"display":"block"});
  $("#list-cerita").css({"display":"block"});
  $("#cersex-lainnya").css({"display":"block"});
  $("#post-blog").css({"display":"none"});
  $("#judul-awal").css({"display":"block"});
  $("#list-cerita").css({"width":"100%"});
  $("#list-cerita").css({"display":"grid"});
  $("#list-cerita").css({"grid-template-rows":"185px auto"});
  $("#list-cerita").css({"grid-template-columns":"50% auto"});
  $("#list-cerita").css({"grid-row-gap":"10px"});
  $("#list-cerita").css({"grid-column-gap":"10px"});
});

//Next post:
$(".next-post").click(function(e){
  e.preventDefault();
  if (thisElement<postLength-1) {
    thisElement = parseInt(thisElement) + 1;
    innerContent(thisElement);
    dissBtn();
  };
});

//Prev post:
$(".prev-post").click(function(e){
  e.preventDefault();
  if (thisElement>0) {
    thisElement = parseInt(thisElement) - 1;
    innerContent(thisElement);
    dissBtn();
  };
});

//Button disable:
function dissBtn(){
  $(".prev-post, .next-post").removeClass("disabled");
  if (thisElement<=0){
    $(".prev-post").addClass("disabled");
  }
  else if (thisElement>=postLength-1){
    $(".next-post").addClass("disabled");
  };
};

