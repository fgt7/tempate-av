$(function() {
  $("#cariEbook").on("keyup", function() {
    var value = $(this).val().toLowerCase();
    if (value.length) {
      $("#list-ebook .book-card").filter(function() {
        $("#list-ebook").show();
        $(this).toggle($(this).text().toLowerCase().indexOf(value) > -1)
      });
    } else {
      $("#list-ebook").hide();
    }
  });
});